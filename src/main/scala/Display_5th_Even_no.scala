
/* display the 5th Even number */
import scala.Byte.MaxValue.==
object Display_5th_Even_no {
  def main(args: Array[String]): Unit =
  {
    for (i <- 0 until 5) {
      val evenNumber = i * 5
      println(s"${i + 1}th even no is : $evenNumber")
    }
    }
  }
